    RÈGLEMENT DU CONCOURS DE CHANT "Notes rigolotes"<br><br>

    Article 1 : Organisation<br>
    La Mairie de Longuenesse organise un concours de chant intitulé "Notes rigolotes". Ce concours a pour but de promouvoir les talents vocaux locaux et de créer une plateforme d'expression artistique pour les participants.<br><br>

    Article 2 : Conditions de Participation<br>
    2.1 Le concours est ouvert à toute personne âgée de 16 ans et plus.<br>
    2.2 Les candidats âgés de 16 à 18 ans doivent fournir une autorisation parentale signée par un parent ou un tuteur légal.<br>
    2.3 Les participants doivent résider dans la commune de Longuenesse ou ses environs immédiats.<br>
    2.4 Les candidats de moins de 16 ans ne sont pas autorisés à participer.<br><br>

    Article 3 : Modalités d'Inscription<br>
    3.1 Les candidats doivent s'inscrire en remplissant le formulaire d'inscription disponible sur le site web de la Mairie de Longuenesse.<br>
    3.2 Les frais d'inscription sont fixés à 50 euros par participant.<br>
    3.3 Le paiement des frais d'inscription doit être effectué lors de l'inscription en ligne. Les modalités de paiement seront précisées sur le site web.<br><br>

    Article 4 : Déroulement du Concours<br>
    4.1 Les auditions se dérouleront en plusieurs étapes, comprenant des sélections préliminaires et une finale.<br>
    4.2 Les dates, lieux et horaires des auditions seront annoncés sur le site web et communiqués aux participants inscrits.<br>
    4.3 Un jury de professionnels sera désigné pour évaluer les performances des participants.<br><br>

    Article 5 : Prix<br>
    5.1 Des prix seront décernés aux participants distingués par le jury, comprenant des récompenses en nature et/ou en espèces.<br>
    5.2 Les prix seront remis lors d'une cérémonie de clôture qui aura lieu à la fin du concours.<br><br>

    Article 6 : Droits d'Image<br>
    6.1 En participant au concours, les candidats autorisent la Mairie de Longuenesse à utiliser leur image, voix et performances à des fins promotionnelles.<br>
    6.2 La Mairie de Longuenesse se réserve le droit de filmer et de photographier les participants pendant le concours.<br><br>

    Article 7 : Dispositions Finales<br>
    7.1 La Mairie de Longuenesse se réserve le droit de modifier le règlement du concours si nécessaire. Les participants seront informés de toute modification.<br>
    7.2 Les décisions du jury sont finales et non susceptibles de recours.<br>
    7.3 Nous ne sommes en aucun cas responsable de la mort de vos enfants.<br>

    <br>Ces règles visent à garantir un environnement respectueux et éthique tout au long du concours, tout en assurant la protection des droits des participants et la transparence dans la communication des résultats.
